use libtock_platform::Register;

/// SyscallLogEntry represents a system call made during test execution.
#[derive(Debug, Eq, PartialEq)]
pub enum SyscallLogEntry {
    // -------------------------------------------------------------------------
    // Yield
    // -------------------------------------------------------------------------
    YieldNoWait,

    YieldWait,

    YieldWaitFor,
    // -------------------------------------------------------------------------
    // Subscribe
    // -------------------------------------------------------------------------
    Subscribe {
        driver_num: u32,
        subscribe_num: u32,
    },

    // -------------------------------------------------------------------------
    // Command
    // -------------------------------------------------------------------------
    Command {
        driver_id: u32,
        command_id: u32,
        argument0: u32,
        argument1: u32,
    },

    // -------------------------------------------------------------------------
    // Read-Only Allow
    // -------------------------------------------------------------------------
    AllowRo {
        driver_num: u32,
        buffer_num: u32,
        len: usize,
    },

    // -------------------------------------------------------------------------
    // Read-Write Allow
    // -------------------------------------------------------------------------
    AllowRw {
        driver_num: u32,
        buffer_num: u32,
        len: usize,
    },

    // -------------------------------------------------------------------------
    // Memop
    // -------------------------------------------------------------------------
    Memop {
        memop_num: u32,
        argument0: Register, // Necessary for Miri ptr provenance of brk()
    },
    // TODO: Add Exit.
}
